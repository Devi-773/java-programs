//converting primitive data type to object called as autobox
class primitive1
{
	public static void main(String[] args)
	{
		int a=10;
		Integer ob=a;
		System.out.println(ob);
		float b=10.1f;
		Float ob1=b;
		System.out.println(ob1);
		double c=10;
		Double ob2=c;
		System.out.println(ob2);
	}

}