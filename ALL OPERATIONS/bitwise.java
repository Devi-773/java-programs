public class bitwise
{
	public static void main(String[] args)
	{
	int a=11,b=10;
	System.out.println("bitwise AND is"+ (a&b));
	System.out.println("bitwise OR is"+ (a|b));
	System.out.println("bitwise EX-OR is"+ (a^b));
	System.out.println("bitwise NOT is"+ (~a));
	System.out.println("bitwise RIGTH-SHIFT is"+ (a>>2));
	System.out.println("bitwise LEFT-SHIFT is"+ (b<<2));
	}
}